
public class Foreachloop {

	public static void main(String[] args) {
      int arr[]= {10,20,30};
      for(int x:arr) {
    	  System.out.println(x);
      }
       String[] names= {"jack","joe","john"};
      for(String name:names) {
    	  System.out.println(name);
      }
      
	}

}
