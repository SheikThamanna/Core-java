import java.util.LinkedHashSet;

public class LinkedHashExample {
	public static void main(String[] args) {
		LinkedHashSet hs=new LinkedHashSet();
		hs.add(200);
		hs.add(60);
		hs.add(160);
		hs.add(40);
		hs.add(80);
		hs.add(140);
		hs.add(180);
		
		System.out.println(hs);
	}

}
